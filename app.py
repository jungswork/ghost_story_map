@app.route("/")
def index():
    """提供前端 HTML 頁面"""
    return send_from_directory(".", "index.html")


@app.route("/api/map-data")
def get_map_data():
    """回傳台灣地圖 SVG 路徑資料（22 縣市）"""
    return jsonify(MAP_DATA)


@app.route("/api/counties")
def get_counties():
    """回傳縣市清單（id + 中文名）"""
    return jsonify([{"id": c["id"], "name": c["name"]} for c in MAP_DATA])


@app.route("/api/stories")
def get_all_stories():
    """回傳全台所有故事（附 countyId 與縣市名）"""
    result = []
    for county_id, story_list in GHOST_STORIES.items():
        for s in story_list:
            result.append({**s, "countyId": county_id, "countyName": COUNTY_NAMES.get(county_id, county_id)})
    return jsonify(result)


@app.route("/api/stories/<county_id>")
def get_stories_by_county(county_id):
    """回傳指定縣市的故事清單"""
    return jsonify(GHOST_STORIES.get(county_id, []))


@app.route("/api/stats")
def get_stats():
    """回傳統計資訊"""
    all_stories = [s for sl in GHOST_STORIES.values() for s in sl]
    tag_counts = {}
    for s in all_stories:
        tag_counts[s["tag"]] = tag_counts.get(s["tag"], 0) + 1
    county_counts = {cid: len(sl) for cid, sl in GHOST_STORIES.items()}
    return jsonify({
        "total": len(all_stories),
        "tag_counts": tag_counts,
        "county_count": len(GHOST_STORIES),
        "county_counts": county_counts,
    })


